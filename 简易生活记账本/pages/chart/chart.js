import * as echarts from '../../ec-canvas/echarts';

const app = getApp();
//绘制图表函数
function initChart(canvas, width, height) {
  const chart = echarts.init(canvas, null, {
    width: width,
    height: height
  });
  canvas.setChart(chart);

  var option = {
    title: {
      left: 'center'
    },
    color: ["#37A2DA"],
    legend: {
      top: 20,
      z: 100
    },
    grid: { 
      containLabel: true
    },
    tooltip: {
      show: true,
      trigger: 'axis'
    },
    xAxis: {
      type: 'category',
      boundaryGap: false,
      data: ['1', '2', '3', '4', '5', '6', '7'],
      // show: false
    },
    yAxis: {
      x: 'center',
      type: 'value',
      splitLine: {
        lineStyle: {
          type: 'dashed'
        }
      }
      // show: false
    },
    series: [{
      name: '消费度',
      type: 'line',
      smooth: true,
      data: getApp().globalData.account,
    },]
  };

  chart.setOption(option);
  return chart;
}

